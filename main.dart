void main() {
  // Declaring a variable with 'var'. It can be changed later.
  var name = 'John';  
  // Declaring a variable with 'final'. It cannot be changed once set.
  final age = 30;     
  // Declaring a constant with 'const'. Its value is set at compilezx   time.
  const PI = 3.14;       
   
  // Printing the values of the variables
  print('Name: $name');  // Output: Name: John
  print('Age: $age');    // Output: Age: 30
  print('PI: $PI');      // Output: PI: 3.14
}